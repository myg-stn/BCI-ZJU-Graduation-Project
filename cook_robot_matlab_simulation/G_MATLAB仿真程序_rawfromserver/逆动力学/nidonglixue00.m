g=jtraj(theta1,theta2,t);%相当于具有tpoly插值的mtraj，但是对多轴情况进行了优化，还允许使用额外参数设置初始和最终速度
[q,qd,qdd]=jtraj(theta1,theta2,t);%通过可选的输出参数，获得随时间变化的关节速度加速度向量
